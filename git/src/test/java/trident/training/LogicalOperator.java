package trident.training;

public class LogicalOperator {

	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		
		System.out.println("a && b = " + (a && b));//and operator
		
		System.out.println("a || b = " + (a || b));// or operator
		
		System.out.println("!a = " + (!a)); //not operator

	}

}
